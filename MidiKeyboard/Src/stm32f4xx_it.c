/**
  * @brief This function handles SPI3 global interrupt.
  */
void SPI3_IRQHandler(void)
{
  /* USER CODE BEGIN SPI3_IRQn 0 */
	if((__HAL_SPI_GET_FLAG(&hspi3, SPI_FLAG_RXNE)))
	{
		switch((uint8_t)hspi3.Instance->DR) {
		case 0b01111111 :
			GPIOA->ODR=(uint16_t)keyboardMatrix[0]<<4;
			break;
		case 0b10111111 :
			GPIOA->ODR=(uint16_t)keyboardMatrix[1]<<4;
			break;
		case 0b11011111 :
			GPIOA->ODR=(uint16_t)keyboardMatrix[2]<<4;
			break;
		case 0b11101111 :
			GPIOA->ODR=(uint16_t)keyboardMatrix[3]<<4;
			break;
		case 0b11110111 :
			GPIOA->ODR=(uint16_t)keyboardMatrix[4]<<4;
			break;
		case 0b11111011 :
			GPIOA->ODR=(uint16_t)keyboardMatrix[5]<<4;
			break;
		case 0b11111101 :
			GPIOA->ODR=(uint16_t)keyboardMatrix[6]<<4;
			break;
		case 0b11111110 :
			GPIOA->ODR=(uint16_t)keyboardMatrix[7]<<4;
			break;
		default:
			GPIOA->ODR=0xFF0;
		}
	}
  /* USER CODE END SPI3_IRQn 0 */
  HAL_SPI_IRQHandler(&hspi3);
  /* USER CODE BEGIN SPI3_IRQn 1 */

  /* USER CODE END SPI3_IRQn 1 */
}

/**
  * @brief This function handles UART4 global interrupt.
  */
void UART4_IRQHandler(void)
{
  /* USER CODE BEGIN UART4_IRQn 0 */
 if ((__HAL_UART_GET_FLAG(&huart4, UART_FLAG_RXNE))) {
		ringBuffer.data[ringBuffer.writeIndex]=(uint8_t)huart4.Instance->DR;
		ringBuffer.writeIndex++;
		ringBuffer.writeIndex&=127;
 }
  /* USER CODE END UART4_IRQn 0 */
  HAL_UART_IRQHandler(&huart4);
  /* USER CODE BEGIN UART4_IRQn 1 */

  /* USER CODE END UART4_IRQn 1 */
}

